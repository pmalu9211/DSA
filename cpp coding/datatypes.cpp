#include<iostream>

int main(){
    float a;
    std::cout<<"hello ";
    std::cout<<a<<std::endl;
    std::cout<<"The size of float a is "<<sizeof(a)<<std::endl;

    int b;
    std::cout<<"hello ";
    std::cout<< b<<std::endl;
    std::cout<<"The size of int b is "<<sizeof(b)<<std::endl;

    short int c;
    std::cout<<"hello ";
    std::cout<<c<<std::endl;
    std::cout<<"The size of long int c is "<<sizeof(c)<<std::endl;
}